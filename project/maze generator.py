maze = t.Turtle()
maze.speed(0)
maze.pensize(2)

def build_maze():
    square_count = 25
    square_size = 20
    start_x = -250
    start_y = 250

    # draw outline of maze
    maze.penup()
    maze.goto(-250,-250)
    maze.pendown()
    maze.forward(480)
    maze.penup()
    maze.forward(20)
    maze.pendown()
    maze.left(90)
    maze.forward(520)
    maze.left(90)
    maze.forward(500)
    maze.penup()
    maze.forward(20)
    maze.pendown()
    maze.left(90)
    maze.forward(520)
    maze.left(90)
    maze.forward(20)
    


    # draw each square
    for row in range(square_count):
        for col in range(square_count):
            #defne square location and size
            x = start_x + col * square_size
            y = start_y - row * square_size
            maze.penup()
            maze.teleport(x, y)

            if random.randint(1,3) == 3:
             #draw top side
                maze.setheading(0)
                maze.pendown()
                maze.forward(square_size)
                maze.penup()
                for loc in maze_grid:
                    loc[y][x][3] = 1

            if random.randint(1,3) == 3:
                # draw right side
                maze.setheading(-90)
                maze.pendown()
                maze.forward(square_size)
                maze.penup()

            if random.randint(1,3) == 3:
                # draw bottom side
                maze.setheading(180)
                maze.pendown()
                maze.forward(square_size)
                maze.penup()

            if random.randint(1,3) == 3:
                # draw left side
                maze.setheading(90)
                maze.pendown()
                maze.forward(square_size)
                maze.penup()
build_maze()
t.done()
